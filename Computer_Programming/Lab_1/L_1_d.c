// C program to calculate area of a triangle with hard coded inputs
#include<stdio.h>
int main()
{
    int length = 5,breadth = 6;
    printf("Length of the rectangle is %d\n",length);
    printf("Breadth of the rectangle is %d\n",breadth);
    printf("The area of rectangle is %d\n",length*breadth);
    return 0;
}